public class Mahasiswa {
	private String namaMhs;
	private String nimMhs;
	private int nilaiMhs;
	
	//methods
	public String getNamaMhs() {
		return namaMhs;
	}
	
	public String getNimMhs() {
		
		return nimMhs;
	}
	
	public int getNilaiMhs() {
		return nilaiMhs;
	}
	
	public void setNamaMhs(String newName) {
		namaMhs = newName;
	}
	
	public void setNimMhs(String newNim) {
		nimMhs = newNim;
	}
	
	public void setNilaiMhs(int newNilai) {
		nilaiMhs = newNilai;
	}
	
}